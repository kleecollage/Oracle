/* SELECT */
SELECT FIRST_NAME, PHONE_NUMBER FROM EMPLOYEES;
SELECT FIRST_NAME AS NOMBRE, JOB_ID AS "Tipo de Trabajo" FROM EMPLOYEES;
SELECT * FROM REGIONS;
SELECT COUNTRY_NAME FROM COUNTRIES;
select street_address as "Dirección", city as "Ciudad", state_province as "Estado" from locations;


/* OPERATORS (+, -, *, /, %) */
SELECT FIRST_NAME, SALARY as "Bruto", SALARY *20/100 AS "Impuestos", salary-(salary*20/100) as "Neto" FROM EMPLOYEES;
select first_name as "Nombre", salary as "Salario", salary*14 as "Salario Anual" from employees;


/* LITERALS */
SELECT 'NAME:' || FIRST_NAME FROM EMPLOYEES;
SELECT 'NAME:' || FIRST_NAME || ' ' || LAST_NAME AS "Name Employee" FROM EMPLOYEES;
SELECT 'El empleado ' || FIRST_NAME || ' del departamento ' || DEPARTMENT_ID || ' tiene un salario de $' || SALARY AS "Employee Info"  FROM EMPLOYEES;
SELECT 'The street ' || STREET_ADDRESS || ' belongs to the city of ' || CITY AS "Street Info" FROM LOCATIONS;


/* DUAL TABLE */
SELECT 4+3, FIRST_NAME FROM EMPLOYEES;
SELECT 4+3 FROM DUAL;
DESC DUAL;


/* NULLS */
    -- Operations with nulls equals null 
SELECT FIRST_NAME, SALARY, COMMISSION_PCT, SALARY * COMMISSION_PCT AS "Total Salary" FROM EMPLOYEES;


/* DISTINCTS */
SELECT FIRST_NAME, DEPARTMENT_ID FROM EMPLOYEES;
