SELECT * FROM LOCATIONS;
SELECT DISTINCT CITY FROM LOCATIONS;
SELECT DISTINCT JOB_ID, DEPARTMENT_ID FROM EMPLOYEES;


/* WHERE */
    -- SELECT <DATA> FROM <TABLE> WHERE <CONDITION>
SELECT * FROM EMPLOYEES WHERE DEPARTMENT_ID = 50;
SELECT * FROM EMPLOYEES WHERE DEPARTMENT_ID = 100;
SELECT * FROM COUNTRIES WHERE REGION_ID = 3;

    -- COMPARISON OPERATORS ( =, >, <, >=, <=, <> ) 
SELECT FIRST_NAME, SALARY FROM EMPLOYEES WHERE SALARY > 5000;
SELECT FIRST_NAME, DEPARTMENT_ID FROM EMPLOYEES WHERE DEPARTMENT_ID <> 50;
SELECT FIRST_NAME, SALARY FROM EMPLOYEES WHERE MANAGER_ID <> 114;


/* LITERAL CONDITIONS */
    -- literals are case sensitive
SELECT FIRST_NAME, LAST_NAME FROM EMPLOYEES WHERE FIRST_NAME='John';
SELECT FIRST_NAME, LAST_NAME FROM EMPLOYEES WHERE FIRST_NAME='john';
SELECT FIRST_NAME, LAST_NAME FROM EMPLOYEES WHERE FIRST_NAME<>'John';
SELECT * FROM EMPLOYEES WHERE LAST_NAME <> 'Smith';
SELECT * FROM EMPLOYEES WHERE JOB_ID = 'ST_CLERK';
SELECT CITY, STREET_ADDRESS FROM LOCATIONS WHERE COUNTRY_ID='US';


/* DATE COMPARISON */
    -- JULIAN NUMBER = 01/01/4712 AC
