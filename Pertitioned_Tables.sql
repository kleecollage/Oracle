/* PARTITIONED TABLES */
-- INSERTS OVER THE MAX VALUE IN PARTITIONS ARE NOT ALLOWED
-- IT IS NOT ALLOWED TO CREATE PARTITIONS BETWEEN VALUES OF TWO PARTITIONS ALREADY CREATED
    /* 
    PARTITION BY RANGE (<COLUMN_1>, ... ,  <COLUMN_n>) {
        PARTITION P1 VALUES LESS THAN (<VALUE_1>),
        PARTITION P2 VALUES LESS THAN (<VALUE_2>),
        PARTITION Pn VALUES LESS THAN (<VALUE_n>),
        PARTITION Pm VALUES LESS THAN (MAXVALUE)
    }
    */
CREATE TABLE range(
    code NUMBER NOT NULL,
    data VARCHAR2(100)
)
    PARTITION BY RANGE(code) (
        PARTITION P1 VALUES LESS THAN (10),
        PARTITION P2 VALUES LESS THAN (20),
        PARTITION P3 VALUES LESS THAN (30),
        PARTITION P4 VALUES LESS THAN (40)
    );

SELECT * FROM USER_PART_TABLES;
SELECT * FROM USER_TAB_PARTITIONS  WHERE TABLE_NAME = 'RANGE';
    
    -- INSERT INTO PARTITIONS
