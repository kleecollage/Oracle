INSERT INTO range VALUES(8, 'bbb');
SELECT * FROM range;
SELECT * FROM range PARTITION(p3);
SELECT * FROM range PARTITION(p1);
SELECT * FROM range WHERE code = 21;
SELECT * FROM range WHERE code > 21;

    -- ADD PARTITIONS
ALTER TABLE range ADD PARTITION P5 VALUES LESS THAN (50);
