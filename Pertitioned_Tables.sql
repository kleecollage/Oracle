ALTER TABLE range ADD PARTITION P6 VALUES LESS THAN (100);

    -- MAXVALUE = Infinite
ALTER TABLE range ADD PARTITION P7 VALUES LESS THAN (MAXVALUE);
INSERT INTO range VALUES(400, 'ddd');

ALTER TABLE range ADD PARTITION P7 VALUES LESS THAN (100); --> Not allowed


    -- UPDATE WITH PARTITION MOVEMENT
ALTER TABLE range ENABLE ROW MOVEMENT; --> leads to loss of performance, so not recommended
SELECT * FROM range;
UPDATE range SET code = 29 WHERE code = 30;

