    -- PARTITIONS BY HASH
    -- MERGE IS NOT ALLOWED WITH HASH PARTITIONS
    /*
    PARTITION BY HASH(<COL_1>, ... , <COL_n>)
    PATITIONS m
    */
CREATE TABLE hash_table
(
    code NUMBER NOT NULL,
    data VARCHAR2(100)
)
    PARTITION BY HASH (code)
    PARTITIONS 3;
    
