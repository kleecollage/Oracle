    /* 
    PARTITION BY LIST (<COLUMN_1>, ... ,  <COLUMN_n>) {
        PARTITION P1 VALUES (<VALUE_1>, <VALUE_2>),
        PARTITION P2 VALUES (<VALUE_3>, <VALUE_4>),
        PARTITION Pn VALUES (<VALUE_5>, <VALUE_6>, <VALUE_7>),
        PARTITION Pm VALUES (DEFAULT)
    }
    */

